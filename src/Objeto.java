public class Objeto {

	private double peso;
	private double lucro;
	private String nome;
	
	public double getPeso() {
		return peso;
	}
	public void setPeso(int peso) {
		this.peso = peso;
	}
	public double getLucro() {
		return lucro;
	}
	public void setLucro(int lucro) {
		this.lucro = lucro;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	
}